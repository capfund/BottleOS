int vesa_init(uint32_t framebuffer_addr, uint32_t width, uint32_t height, uint32_t pitch, uint8_t bpp) {
    framebuffer = (uint8_t *)framebuffer_addr;
    screen_width = width;
    screen_height = height;
    screen_pitch = pitch;
    screen_bpp = bpp;

    term_width = width / 8;
    term_height = height/8;

    term_col = 0;
    term_row = 0;
    
    return 0;
}

uint32_t vesa_rgb(uint8_t red, uint8_t green, uint8_t blue) {
    if (screen_bpp == 32) {
        return (red << 16) | (green << 8) | blue; 
    } else if (screen_bpp == 24) {
        return (red << 16) | (green << 8) | blue;
    }

    return 0;
}

void vesa_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (x >= screen_width || y >= screen_height) return;
    
    uint32_t offset = y * screen_pitch + x * (screen_bpp / 8);
    
    if (screen_bpp == 32) {
        *((uint32_t*)(framebuffer + offset)) = color;
    } else if (screen_bpp == 24) {
        framebuffer[offset] = color & 0xFF;
        framebuffer[offset + 1] = (color >> 8) & 0xFF;
        framebuffer[offset + 2] = (color >> 16) & 0xFF;
    }
}


void vesa_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    for (uint32_t i = 0; i < h; i++) {
        for (uint32_t j = 0; j < w; j++) {
            vesa_put_pixel(x + j, y + i, color);
        }
    }
}

void vesa_clear_screen(uint32_t color) {
    vesa_fill_rect(0, 0, screen_width, screen_height, color);
}


void vesa_putchar(char c, uint32_t x, uint32_t y, uint32_t fg, uint32_t bg) {
    if ((unsigned char)c > 127) c = '?';
    
    const uint8_t *glyph = font8x8[(int)c];
    
    for (int i = 0; i < 8; i++) {
        for (int j = 0; j < 8; j++) {
            if (glyph[i] & (1 << j)) {
                vesa_put_pixel(x + j, y + i, fg);
            } else {
                vesa_put_pixel(x + j, y + i, bg);
            }
        }
    }
}

void vesa_putstr(const char *str, uint32_t x, uint32_t y, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    for (int i = 0; str[i] != '\0'; i++) {
        if (str[i] == '\n') {
            cx = x;
            y += 8;
            continue;
        }
        vesa_putchar(str[i], cx, y, fg, bg);
        cx += 8;
    }
}

// Terminal emulation functions
void vesa_term_init(void) {
    term_col = 0;
    term_row = 0;
    vesa_clear_screen(vesa_rgb(0, 0, 0));
}

static void vesa_term_scroll(void) {
    // Copy rows up
    for (uint32_t y = 8; y < screen_height; y++) {
        for (uint32_t x = 0; x < screen_width; x++) {
            uint32_t src_offset = y * screen_pitch + x * (screen_bpp / 8);
            uint32_t dst_offset = (y - 8) * screen_pitch + x * (screen_bpp / 8);
            
            if (screen_bpp == 32) {
                *((uint32_t*)(framebuffer + dst_offset)) = *((uint32_t*)(framebuffer + src_offset));
            } else if (screen_bpp == 24) {
                framebuffer[dst_offset] = framebuffer[src_offset];
                framebuffer[dst_offset + 1] = framebuffer[src_offset + 1];
                framebuffer[dst_offset + 2] = framebuffer[src_offset + 2];
            }
        }
    }
    
    // Clear bottom row
    vesa_fill_rect(0, screen_height - 8, screen_width, 8, vesa_rgb(0, 0, 0));
    
    if (term_row > 0) term_row--;
}

void vesa_term_putchar(char c, uint32_t color) {
    if (c == '\n') {
        term_col = 0;
        term_row++;
        if (term_row >= term_height) {
            vesa_term_scroll();
        }
        return;
    }
    
    if (c == '\b') {
        if (term_col > 0) {
            term_col--;
            vesa_putchar(' ', term_col * 8, term_row * 8, color, vesa_rgb(0, 0, 0));
        }
        return;
    }
    
    vesa_putchar(c, term_col * 8, term_row * 8, color, vesa_rgb(0, 0, 0));
    term_col++;
    
    if (term_col >= term_width) {
        term_col = 0;
        term_row++;
        if (term_row >= term_height) {
            vesa_term_scroll();
        }
    }
}

void vesa_term_putstr(const char *str, uint32_t color) {
    for (int i = 0; str[i] != '\0'; i++) {
        vesa_term_putchar(str[i], color);
    }
}

void vesa_term_clear(void) {
    vesa_clear_screen(vesa_rgb(0, 0, 0));
    term_col = 0;
    term_row = 0;
}